<script>
// Price Calculator
let basePrice = 2000;
let addonTotal = 0;

function updatePrice() {
    const checkboxes = document.querySelectorAll('.feature-checkbox:checked');
    addonTotal = 0;
    let selectedFeatures = [];
    
    checkboxes.forEach(checkbox => {
        addonTotal += parseInt(checkbox.dataset.price);
        selectedFeatures.push(checkbox.id.replace('-', ' ').replace(/\b\w/g, l => l.toUpperCase()));
    });
    
    const totalPrice = basePrice + addonTotal;
    
    document.getElementById('addon-total').textContent = addonTotal;
    document.getElementById('total-price').textContent = totalPrice;
    document.getElementById('modal-addon-total').textContent = addonTotal;
    document.getElementById('modal-total-price').textContent = totalPrice;
    document.getElementById('total-cost').value = totalPrice;
    document.getElementById('features').value = selectedFeatures.join(', ');
}

// Add event listeners to checkboxes
document.querySelectorAll('.feature-checkbox').forEach(checkbox => {
    checkbox.addEventListener('change', updatePrice);
});

// Open quote modal
function openQuoteModal() {
    updatePrice();
    const modal = new bootstrap.Modal(document.getElementById('quoteModal'));
    modal.show();
}

// Form submission
document.getElementById('quoteForm').addEventListener('submit', function(e) {
    e.preventDefault();

    // Loader logic start
    const submitBtn = document.querySelector('button[form="quoteForm"]');
    submitBtn.disabled = true;
    const originalBtnHtml = submitBtn.innerHTML;
    submitBtn.innerHTML = `<span class="spinner-border spinner-border-sm me-2" role="status" aria-hidden="true"></span>Submitting...`;

    const formData = new FormData(this);

    fetch('/controllers/website_submit.php', {
        method: 'POST',
        body: formData
    })
    .then(response => response.json())
    .then(data => {
        submitBtn.disabled = false;
        submitBtn.innerHTML = originalBtnHtml; // Restore button
        if(data.success) {
            // Show success message
            const modal = bootstrap.Modal.getInstance(document.getElementById('quoteModal'));
            modal.hide();
            
            // Show success alert
            const alertDiv = document.createElement('div');
            alertDiv.className = 'alert alert-success alert-dismissible fade show position-fixed';
            alertDiv.style.cssText = 'top: 20px; right: 20px; z-index: 9999; min-width: 300px;';
            alertDiv.innerHTML = `
                <strong>Success!</strong> Your quote request has been submitted successfully. We'll contact you soon!
                <button type="button" class="btn-close" data-bs-dismiss="alert"></button>
            `;
            document.body.appendChild(alertDiv);
            
            // Auto remove alert after 5 seconds
            setTimeout(() => {
                if(alertDiv.parentNode) {
                    alertDiv.remove();
                }
            }, 5000);
            
            // Reset form
            document.getElementById('quoteForm').reset();
            updatePrice();
        } else {
            // Show error message
            const alertDiv = document.createElement('div');
