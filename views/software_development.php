<script>
// Price calculation functionality
let basePrice = 5000;
let additionalPrice = 0;

// Base package selection
document.querySelectorAll('.base-feature').forEach(card => {
    card.addEventListener('click', function() {
        // Remove active class from all base features
        document.querySelectorAll('.base-feature').forEach(c => c.classList.remove('active'));
        
        // Add active class to clicked card
        this.classList.add('active');
        
        // Update base price
        basePrice = parseInt(this.dataset.price);
        updatePrice();
    });
});

// Additional features selection
document.querySelectorAll('.feature-toggle').forEach(checkbox => {
    checkbox.addEventListener('change', function() {
        updatePrice();
    });
});

function updatePrice() {
    // Calculate additional features price
    additionalPrice = 0;
    document.querySelectorAll('.feature-toggle:checked').forEach(checkbox => {
        const featureItem = checkbox.closest('.feature-item');
        additionalPrice += parseInt(featureItem.dataset.price);
    });
    
    // Calculate totals
    const subtotal = basePrice + additionalPrice;
    const gstAmount = Math.round(subtotal * 0.18);
    const total = subtotal + gstAmount;
    
    // Update display
    document.getElementById('basePrice').textContent = `₹${basePrice.toLocaleString()}`;
    document.getElementById('additionalPrice').textContent = `₹${additionalPrice.toLocaleString()}`;
    document.getElementById('subtotal').textContent = `₹${subtotal.toLocaleString()}`;
    document.getElementById('gstAmount').textContent = `₹${gstAmount.toLocaleString()}`;
    document.getElementById('totalPrice').textContent = `₹${total.toLocaleString()}`;
    
    // Update modal prices
    document.getElementById('modalSubtotal').textContent = `₹${subtotal.toLocaleString()}`;
    document.getElementById('modalGST').textContent = `₹${gstAmount.toLocaleString()}`;
    document.getElementById('modalTotal').textContent = `₹${total.toLocaleString()}`;
}

// Open quote modal
function openQuoteModal() {
    updatePrice();
    
    // Collect selected features
    const selectedFeatures = [];
    
    // Get base package
    const activeBase = document.querySelector('.base-feature.active');
    if (activeBase) {
        const baseType = activeBase.querySelector('.feature-header span').textContent;
        selectedFeatures.push(`Base Package: ${baseType} (₹${basePrice})`);
    }
    
    // Get additional features
    document.querySelectorAll('.feature-toggle:checked').forEach(checkbox => {
        const label = checkbox.nextElementSibling.textContent;
        const price = checkbox.closest('.feature-item').dataset.price;
        selectedFeatures.push(`${label} (₹${price})`);
    });
    
    // Update hidden fields
    document.getElementById('selectedFeatures').value = selectedFeatures.join(', ');
    document.getElementById('totalAmount').value = basePrice + additionalPrice + Math.round((basePrice + additionalPrice) * 0.18);
    
    const modal = new bootstrap.Modal(document.getElementById('quoteModal'));
    modal.show();
}

// Form submission
document.getElementById('quoteForm').addEventListener('submit', function(e) {
    e.preventDefault();
    
    const submitBtn = document.getElementById('submitBtn');
    const btnText = submitBtn.querySelector('.btn-text');
    const btnLoader = submitBtn.querySelector('.btn-loader');
    
    // Show loader
    btnText.classList.add('d-none');
    btnLoader.classList.remove('d-none');
    submitBtn.disabled = true;
    
    const formData = new FormData(this);
    
    fetch('/controllers/software_submit.php', {
        method: 'POST',
        body: formData
    })
    .then(response => response.json())
    .then(data => {
        // Hide loader
        btnText.classList.remove('d-none');
        btnLoader.classList.add('d-none');
        submitBtn.disabled = false;
        
        if(data.success) {
            // Show success message
            const modal = bootstrap.Modal.getInstance(document.getElementById('quoteModal'));
            modal.hide();
            
            // Show success alert
            const alertDiv = document.createElement('div');
            alertDiv.className = 'alert alert-success alert-dismissible fade show position-fixed';
            alertDiv.style.cssText = 'top: 20px; right: 20px; z-index: 9999; min-width: 300px;';
            alertDiv.innerHTML = `
                <strong>Success!</strong> Your software development quote request has been submitted successfully. We'll contact you soon!
                <button type="button" class="btn-close" data-bs-dismiss="alert"></button>
            `;
            document.body.appendChild(alertDiv);
            
            // Auto remove alert after 5 seconds
            setTimeout(() => {
                if(alertDiv.parentNode) {
                    alertDiv.remove();
                }
            }, 5000);
            
            // Reset form
            document.getElementById('quoteForm').reset();
            updatePrice();
        } else {
            // Show error message
            const alertDiv = document.createElement('div');
            alertDiv.className = 'alert alert-danger alert-dismissible fade show position-fixed';
            alertDiv.style.cssText = 'top: 20px; right: 20px; z-index: 9999; min-width: 300px;';
            alertDiv.innerHTML = `
                <strong>Error!</strong> ${data.message || 'Something went wrong. Please try again.'}
                <button type="button" class="btn-close" data-bs-dismiss="alert"></button>
            `;
            document.body.appendChild(alertDiv);
            
            // Auto remove alert after 5 seconds
            setTimeout(() => {
                if(alertDiv.parentNode) {
                    alertDiv.remove();
                }
            }, 5000);
        }
    })
    .catch(error => {
        console.error('Error:', error);
        
        // Hide loader
        btnText.classList.remove('d-none');
        btnLoader.classList.add('d-none');
        submitBtn.disabled = false;
        
        // Show error message
        const alertDiv = document.createElement('div');
        alertDiv.className = 'alert alert-danger alert-dismissible fade show position-fixed';
        alertDiv.style.cssText = 'top: 20px; right: 20px; z-index: 9999; min-width: 300px;';
        alertDiv.innerHTML = `
            <strong>Error!</strong> Network error. Please check your connection and try again.
            <button type="button" class="btn-close" data-bs-dismiss="alert"></button>
        `;
        document.body.appendChild(alertDiv);
        
        // Auto remove alert after 5 seconds
        setTimeout(() => {
            if(alertDiv.parentNode) {
                alertDiv.remove();
            }
        }, 5000);
    });
});

